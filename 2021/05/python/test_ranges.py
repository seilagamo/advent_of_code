def test_positive_ranges():
    print()
    for i in range(0, 10):
        print(i)


def test_positive_ranges_2():
    print()
    for i in range(0, 10 + 1):
        print(i)


def test_negative_ranges():
    print()
    for i in range(10, 0, -1):
        print(i)


def test_negative_ranges_2():
    print()
    for i in range(10, 0 - 1, -1):
        print(i)
